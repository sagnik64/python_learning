import math

print(f'math.pi = {math.pi}')
print(f'math.acos(10.5) = {math.acos(1)}')
print(f'math.sin(120) = {math.sin(120)}')
print(f'math.ceil(10.7) = {math.ceil(10.7)}')
print(f'math.floor(10.7) = {math.floor(10.7)}')
print(f'math.gcd(12,18) = {math.gcd(12,30)}')
print(f'math.fabs(-10) = {math.fabs(-10)}')
